# Variablen werden direkt erstellt wenn man einen Wert zuweist

x = 5
y = "Mustafa"

print(x)
print(y)

# Man kann werte sowie Datentypen verändern, der Datentyp passt sich dem Wert an

x = "test"

print(x)

# Casting = Wenn man den Datentyp definieren will macht man das folgendermaßen: 

x = int(3) #beispiel integer

print(x)

# Mit der type() funktion kriegt man den Datentypen raus:

print(type(x))

# Strings könenn mit einem Zitatstrich, oder Gänsefüßchen gemacht werden

y = "hi"

print(y)

y = 'hallo'

print (y)

